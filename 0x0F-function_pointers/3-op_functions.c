#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: first int
 * @b: second int
 * Return: the difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muliplies two integers
 * @a: first int
 * @b: second int
 * Return: the product of the two integers
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divieds two integers
 * @a: first int
 * @b: second int
 * Return: result of the division
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two integers
 * @a: first int
 * @b: second int
 * Return: the reminder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
